class Alphabate{
    public static void main(String []args)
    {
       char value = '^';
        if(value>='A' && value<='Z' || value>='a' && value<='z')
         {
            System.out.println(value+ " = is Alphabate");
          }
      
         else
            
           {
            System.out.println(value+ " = is not Alphabate");
          }
       }
}
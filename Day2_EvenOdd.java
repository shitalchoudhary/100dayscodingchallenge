class EvenOdd
{
                        public static void main(String []args)
                        {
                            
                              int S = 11;
                          
                          if(S%2==0)
                               {
                                      System.out.println(S+ " is even number");
                                }
                         
                          else
                           
                                 {
                                       System.out.println(S+ " is odd number");
                                  }

                          }
}       